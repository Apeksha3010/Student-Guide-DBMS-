    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this student?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            String usn = jTextField11.getText();
            try {
                Connection con = ConnectionProvider.getCon();
                String sql;
                Statement st = con.createStatement();
                sql = "update student set guide_id=NULL where usn ='" +usn+ "';";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Guide field of student has been emptied.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

