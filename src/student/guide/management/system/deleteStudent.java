    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usn= jTextField11.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            String sql;
            Statement st = con.createStatement();
            ResultSet rs;
            sql="select name,phone,branch,section,semester,s.pin,cname,state,language,guide_id from student s, city c where s.pin=c.pin and usn ='"+usn+"';";
            rs=st.executeQuery(sql);
            if (rs.next()) {
                jTextField1.setText(rs.getString("name"));
                jTextField2.setText(usn);
                jTextField3.setText(rs.getString("phone"));
                jTextField4.setText(rs.getString("branch"));
                jTextField5.setText(rs.getString("section"));
                jTextField6.setText(rs.getString("semester"));
                jTextField7.setText(rs.getString("cname"));
                jTextField8.setText(rs.getString("state"));
                jTextField9.setText(rs.getString("s.pin"));
                jTextField10.setText(rs.getString("language"));
                //jTextArea1.setText("I dont know");

                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jTextField4.setEditable(false);
                jTextField5.setEditable(false);
                jTextField6.setEditable(false);
                jTextField7.setEditable(false);
                jTextField8.setEditable(false);
                jTextField9.setEditable(false);
                jTextField10.setEditable(false);
                jButton3.setVisible(true);

            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

