    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        try{ 
            Connection con = ConnectionProvider.getCon();
            String sql;
            Statement st = con.createStatement();
            ResultSet rs;
            sql = "select * from guide where guide_id ='"+guide_id+"';";
            rs=st.executeQuery(sql);
            if(rs.next()){
                jTextField1.setText(rs.getString("gname"));
                jTextField2.setText(rs.getString("guide_id"));
                jTextField3.setText(rs.getString("email"));
                jTextField4.setText(rs.getString("designation"));
                jTextArea1.setText(rs.getString("achievements"));
                jTextArea2.setText(rs.getString("hobbies"));
                
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jTextField4.setEditable(false);
                jTextArea1.setEditable(false);
                jTextArea2.setEditable(false);
                
                jTextField1.setBackground(cl);
                jTextField2.setBackground(cl);
                jTextField3.setBackground(cl);
                jTextField4.setBackground(cl);
                jTextArea1.setBackground(cl);
                jTextArea2.setBackground(cl);
                
            }
        }
        catch(Exception e){
            String ex=e.getMessage();
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton10ActionPerformed

