    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int a=JOptionPane.showConfirmDialog(null, "Are you sure you want to sign out?", "Select", JOptionPane.YES_NO_OPTION);
        if(a==0){
            try{ 
            Connection con = ConnectionProvider.getCon();
            String sql;
            Statement st = con.createStatement();
                sql="delete from guide where guide_id ='"+this.guide_id+"';";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Sucessfully signed out.");
            setVisible(false);
            new login().setVisible(true);
            }
            catch(HeadlessException | SQLException e){
                String ex=e.getMessage();
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

